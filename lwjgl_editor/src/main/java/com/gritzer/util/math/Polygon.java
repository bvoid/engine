package com.gritzer.util.math;

public class Polygon {

  protected Color color;
  protected float[] xPoints;
  protected float[] yPoints;
  protected float x;
  protected float y;
  protected float r;

  protected float w;
  protected float h;

  public Color getColor() {
    return color;
  }

  public void setColor(Color color) {
    this.color = color;
  }

  public float[] getxPoints() {
    return xPoints;
  }

  public void setxPoints(float[] xPoints) {
    this.xPoints = xPoints;
  }

  public float[] getyPoints() {
    return yPoints;
  }

  public void setYPoints(float[] yPoints) {
    this.yPoints = yPoints;
  }

  public float getX() {
    return x;
  }

  public void setX(float x) {
    this.x = x;
  }

  public float getY() {
    return y;
  }

  public void setY(float y) {
    this.y = y;
  }

  public float getR() {
    return r;
  }

  public void setR(float r) {
    this.r = r;
  }

  public float getW() {
    return w;
  }

  public void setW(float w) {
    this.w = w;
  }

  public float getH() {
    return h;
  }

  public void setH(float h) {
    this.h = h;
  }

}
